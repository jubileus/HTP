package com.bjtu.model.bo;

public class PathEntity {
	//路径名称
	private String name;
	//路径数值
	private String path;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	
}
